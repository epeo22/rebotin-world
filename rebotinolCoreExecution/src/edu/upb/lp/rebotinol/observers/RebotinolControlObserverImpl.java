package edu.upb.lp.rebotinol.observers;


/**
 * Basic implementation of the {@link RebotinolControlObserver} that does
 * nothing when observing events.
 * 
 * @author Alexis Marechal
 * 
 */
public abstract class RebotinolControlObserverImpl {
	/**
	 * {@inheritDoc}
	 */
	public void startPlay() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void stopPlay() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void activatePlay() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void deActivatePlay() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void activatePrevious() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void deActivatePrevious() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void activateNext() {
		// do nothing
	}

	/**
	 * {@inheritDoc}
	 */
	public void deActivateNext() {
		// do nothing
	}
}
